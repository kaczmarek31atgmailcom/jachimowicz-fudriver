CREATE TABLE payroll_transaction
(
  id               INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
  uuid             VARCHAR(60),
  person_id        INT,
  person_name      VARCHAR(60)
                   CHARACTER SET utf8mb4,
  person_surname   VARCHAR(60)
                   CHARACTER SET utf8mb4,
  amount           INT,
  creator_login    VARCHAR(60)
                   CHARACTER SET utf8mb4,
  creator_id       INT,
  creator_name     VARCHAR(60)
                   CHARACTER SET utf8mb4,
  creator_surname  VARCHAR(60)
                   CHARACTER SET utf8mb4,
  salary_header_id INT,
  transaction_type INT,
  date             DATE,
  dtype            VARCHAR(100)
);


CREATE TABLE payroll_month (
  id        INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
  uuid      VARCHAR(60),
  first_day DATE,
  is_closed TINYINT,
  UNIQUE KEY (first_day)
);

CREATE TABLE monthly_payoff_header (
  id            INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
  uuid          VARCHAR(60),
  month_id      INT,
  user_id       INT,
  creation_date DATE,
  FOREIGN KEY (month_id) REFERENCES payroll_month (id),
  FOREIGN KEY (user_id) REFERENCES users (id)
);

CREATE TABLE monthly_payoff_detail (
  id             INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
  uuid           VARCHAR(60),
  header_id      INT,
  person_id      INT,
  person_name    VARCHAR(100)
                 CHARACTER SET utf8mb4,
  person_surname VARCHAR(100)
                 CHARACTER SET utf8mb4,
  month_id       INT,
  amount         INT,
  payroll_type   INT,
  FOREIGN KEY (header_id) REFERENCES monthly_payoff_header (id),
  FOREIGN KEY (person_id) REFERENCES ludzie (id),
  FOREIGN KEY (month_id) REFERENCES payroll_month (id)
);



CREATE TABLE payroll_bonus
(
  id     INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
  uuid   VARCHAR(60),
  name   VARCHAR(100)
         CHARACTER SET utf8mb4,
  param  INT,
  active INT,
  dtype  VARCHAR(60)
);

CREATE TABLE monthly_payoff_detail_bonus
(
  id        INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
  uuid      VARCHAR(60),
  detail_id INT,
  bonus_id  INT,
  FOREIGN KEY (detail_id) REFERENCES monthly_payoff_detail (id),
  FOREIGN KEY (bonus_id) REFERENCES payroll_bonus (id)
);

CREATE TABLE bonus_person_month
(
  id               INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
  uuid             VARCHAR(60),
  person_id        INT NOT NULL,
  payroll_month_id INT NOT NULL,
  bonus_id         INT NOT NULL,
  FOREIGN KEY (person_id) REFERENCES ludzie (id),
  FOREIGN KEY (payroll_month_id) REFERENCES payroll_month (id),
  FOREIGN KEY (bonus_id) REFERENCES payroll_bonus (id),
  UNIQUE (person_id,payroll_month_id,bonus_id)
);

CREATE TABLE harvest_salary (
  id               INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
  uuid             VARCHAR(60),
  payoff_detail_id INT,
  salary_day_type  INT,
  type_id          INT,
  person_id        INT,
  wage_id          INT,
  month_id         INT,
  wage_value       INT,
  amount_kg        INT,
  amount_money     INT,
  FOREIGN KEY (payoff_detail_id) REFERENCES monthly_payoff_detail (id),
  FOREIGN KEY (person_id) REFERENCES ludzie (id),
  FOREIGN KEY (type_id) REFERENCES rodzaj (id),
  FOREIGN KEY (wage_id) REFERENCES wage (id),
  FOREIGN KEY (month_id) REFERENCES payroll_month (id)
);

CREATE TABLE work_time_salary (
  id               INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
  uuid             VARCHAR(60),
  payoff_detail_id INT,
  person_id        INT,
  regular_rate     INT,
  regular_minutes  INT,
  sunday_rate      INT,
  sunday_minutes   INT,
  bonus_rate       INT,
  bonus_minutes    INT,
  month_id         INT,
  FOREIGN KEY (payoff_detail_id) REFERENCES monthly_payoff_detail (id),
  FOREIGN KEY (person_id) REFERENCES ludzie (id),
  FOREIGN KEY (month_id) REFERENCES payroll_month (id)
);

CREATE TABLE bonus_salary (
  id               INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
  uuid             VARCHAR(60),
  payoff_detail_id INT,
  month_id         INT,
  person_id        INT,
  bonus_id         INT,
  amount_money     INT,
  FOREIGN KEY (payoff_detail_id) REFERENCES monthly_payoff_detail (id),
  FOREIGN KEY (person_id) REFERENCES ludzie (id),
  FOREIGN KEY (month_id) REFERENCES payroll_month (id),
  FOREIGN KEY (bonus_id) REFERENCES payroll_bonus (id)
);

